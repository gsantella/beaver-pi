<p>The webserver that you are reading this page from is running on the <b>2012-10-18 Raspbian Weezy</b> distribution.  Installation of the <b>OS</b>
is rather easy, and can be completed in a few steps.  Several <b>YouTube</b> videos can be found that completely take the user through the process,
however I will briefly outline the steps towards getting your <b>Pi</b> up and running.</p>
<ol>
	<li>Download the most current <b><i>Raspbian</i> ISO image</b> from <a href="http://www.raspberrypi.org/downloads">here</a>.</li>
	<li>Properly format your SD card using <a href="https://www.sdcard.org/downloads/formatter_3/">this</a> utility. (Mac/PC)</li>
	<li>Install the <b>ISO</b> image to your SD card with <a href="https://launchpad.net/win32-image-writer/">this</a> tool. (PC)</li>
	<li>Insert SD card into the <b>Pi</b>. Plug all cables into the <b>Pi</b>, plugging in the power cord last.</b>
	<li><b>Pi</b> boots up, login from <b>SSH</b> or local keyboard if you have a monitor set up. (User: <b>pi</b> Password: <b>raspberry</b>)</li>
	
</ol>
	